package ir.component.web.controller;

/**
 * @author Mohammad Yasin Kaji
 */
public class ComponentSecurityException extends ComponentException {

    public ComponentSecurityException() {
        super();
    }

    public ComponentSecurityException(String message, Throwable cause) {
        super(message, cause);
    }

    public ComponentSecurityException(String message) {
        super(message);
    }

    public ComponentSecurityException(Throwable cause) {
        super(cause);
    }

}
